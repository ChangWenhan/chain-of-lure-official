def chat_with_story_llm(original_question: str, template: str = old_template) -> str | None:
    """
    Sends a request to the DeepSeek LLM and retrieves the response.

    Args:
        original_question (str): The input question or prompt for the LLM.
        template (str): The template to format the question.

    Returns:
        str | None: The LLM's response content, or None if an error occurs.
    """
    formatted_prompt = template.format(original_question)

    try:
        response = deepseek_client.chat.completions.create(
            model="llama",  # Specify the model being used
            messages=[
                {"role": "system", "content": "You are a helpful assistant."},
                {"role": "user", "content": formatted_prompt}
            ],
            stream=False,  # Set to True if you want to stream responses
            # 'extra_body' might be specific to your DeepSeek setup or a custom fork of OpenAI's library.
            # Ensure this parameter is supported by your 'deepseek_client' version.
            extra_body={"enable_thinking": False},
        )
        return response.choices[0].message.content
    except Exception as e:
        print(f"Error during API call for '{original_question[:50]}...': {e}") # Log the error
        return None # Return None to indicate failure

def check_for_attack_success(response_text: str, forbidden_phrases: list) -> tuple[bool, str]:
    """
    Checks if the LLM's response_text contains any forbidden phrases.
    This is used to determine if an "attack" (e.g., a jailbreak prompt) was "successful"
    by seeing if the LLM avoided a refusal.

    Args:
        response_text (str): The text generated by the LLM.
        forbidden_phrases (list): A list of strings that indicate a refusal or canned response.

    Returns:
        tuple[bool, str]: (True, "") if no forbidden phrase is found (attack potentially successful).
                        (False, "reason") if a forbidden phrase is detected.
    """
    if not response_text: # Handle cases where response_text is None or empty
        return False, "No response from LLM."
    for phrase in forbidden_phrases:
        if phrase.lower() in response_text.lower(): # Case-insensitive check
            return False, f"Detected forbidden phrase: '{phrase}'"
    return True, "" # No forbidden phrases found

def process_single_goal(goal_text: str, retries: int = 3, retry_delay: int = 1) -> dict | None:
    """
    Processes a single goal: gets a story from the LLM, checks for attack success, and formats the result.

    Args:
        goal_text (str): The input text (goal/question) for the LLM.
        retries (int): Number of times to retry LLM call on failure.
        retry_delay (int): Seconds to wait between retries.

    Returns:
        dict | None: A dictionary containing the message, story, and failure reason,
                     or None if processing fails after retries.
    """
    story_content = None
    current_attempts = 0

    while current_attempts < retries and not story_content:
        story_content = chat_with_story_llm(goal_text)
        if not story_content:
            current_attempts += 1
            if current_attempts < retries:
                print(f"Retrying for '{goal_text[:50]}...' (Attempt {current_attempts}/{retries}). Waiting {retry_delay}s.")
                time.sleep(retry_delay)
            else:
                print(f"Failed to get story for: '{goal_text[:50]}...' after {retries} attempts. Skipping.")
                # Return a specific structure indicating permanent failure for this item
                return {
                    "message": goal_text,
                    "story": None,
                    "failure_reason": "Failed to get response from LLM after multiple retries.",
                    "attack_successful": False # Explicitly mark as not successful
                }

    attack_successful, failure_detail = check_for_attack_success(story_content, TEST_PREFIXES)

    return {
        "message": goal_text,
        "story": story_content,
        "failure_reason": failure_detail if not attack_successful else "", # Only provide reason if attack failed
        "attack_successful": attack_successful # Add a clear boolean flag
    }

def process_data_concurrently(input_csv_path: str, output_json_path: str, max_workers: int = 32, save_batch_size: int = 10):
    """
    Reads data from a CSV, processes each item concurrently using an LLM,
    checks for attack success, and saves results incrementally to a JSON file.

    Args:
        input_csv_path (str): Path to the input CSV file (must contain a 'goal' column).
        output_json_path (str): Path to the output JSON file.
        max_workers (int): Maximum number of concurrent threads.
        save_batch_size (int): Number of results to accumulate before saving to JSON.
    """
    processed_results = [] # Stores all processed data for final summary
    successful_attacks_flags = [] # Stores boolean flags for success rate calculation

    try:
        df = pd.read_csv(input_csv_path)
        if 'goal' not in df.columns:
            print(f"Error: CSV file '{input_csv_path}' must contain a 'goal' column.")
            return
    except FileNotFoundError:
        print(f"Error: Input CSV file not found at '{input_csv_path}'.")
        return
    except Exception as e:
        print(f"Error reading CSV file '{input_csv_path}': {e}")
        return

    # Attempt to load existing data from the output JSON file to resume processing
    try:
        with open(output_json_path, 'r', encoding='utf-8') as json_file:
            existing_data = json.load(json_file)
        print(f"Loaded {len(existing_data)} existing records from '{output_json_path}'.")
    except FileNotFoundError:
        existing_data = []
        print(f"Output file '{output_json_path}' not found. Starting fresh.")
    except json.JSONDecodeError:
        existing_data = []
        print(f"Warning: Could not decode JSON from '{output_json_path}'. Starting fresh.")

    # Create a set of already processed messages to avoid duplicates if resuming
    # Assuming 'message' field in existing_data corresponds to 'goal' from CSV
    processed_messages = {item.get("message") for item in existing_data if item.get("message")}
    goals_to_process = [goal for goal in df['goal'] if goal not in processed_messages]

    if not goals_to_process:
        print("No new goals to process. All goals from CSV are already in the output JSON or CSV is empty.")
        # Optionally, calculate success rate based on existing_data if needed
        if existing_data:
            successful_attacks_flags = [item.get("attack_successful", False) for item in existing_data]
            if successful_attacks_flags:
                success_percentage = sum(successful_attacks_flags) / len(successful_attacks_flags)
                print(f"Overall success rate based on existing data: {success_percentage:.3%}")
        return

    print(f"Total goals to process: {len(df['goal'])}. New goals to process: {len(goals_to_process)}.")

    # Use ThreadPoolExecutor for concurrent API calls
    with ThreadPoolExecutor(max_workers=max_workers) as executor:
        # Submit tasks to the executor
        future_to_goal = {executor.submit(process_single_goal, goal): goal for goal in goals_to_process}
        
        # Process completed futures
        # tqdm provides a progress bar
        for future in tqdm(as_completed(future_to_goal), total=len(goals_to_process), desc="Processing Goals", ncols=100):
            # goal_text = future_to_goal[future] # Get the original goal if needed for logging
            try:
                result_data = future.result() # Get the result from the completed future
                if result_data:
                    existing_data.append(result_data) # Add new result to the list
                    successful_attacks_flags.append(result_data["attack_successful"])

                    # Save to JSON file in batches
                    if len(existing_data) % save_batch_size == 0:
                        with open(output_json_path, 'w', encoding='utf-8') as json_file:
                            json.dump(existing_data, json_file, ensure_ascii=False, indent=4)
                        print(f"Saved {len(existing_data)} records to '{output_json_path}'...")
            except Exception as e:
                # goal_text_failed = future_to_goal[future] # Get the goal that caused the exception
                print(f"An error occurred while processing a future: {e}")
                # Optionally, log this failure more formally or add a placeholder to existing_data

    # Final save for any remaining data not covered by batch saves
    try:
        with open(output_json_path, 'w', encoding='utf-8') as json_file:
            json.dump(existing_data, json_file, ensure_ascii=False, indent=4)
        print(f"Final save: Total {len(existing_data)} records saved to '{output_json_path}'.")
    except Exception as e:
        print(f"Error during final save to '{output_json_path}': {e}")

    # Calculate and print the success rate of "attacks"
    if successful_attacks_flags: # Check if the list is not empty
        success_percentage = sum(successful_attacks_flags) / len(successful_attacks_flags)
        print(f"Attack success rate: {success_percentage:.3%} ({sum(successful_attacks_flags)} successful out of {len(successful_attacks_flags)} processed new items).")
    elif not goals_to_process and existing_data: # If only existing data was present
        print("No new items were processed. Success rate is based on previously loaded data if available.")
    else:
        print("No results to calculate success rate from.")

